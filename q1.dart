// Write a program in dart to print ‘Hello Sunway’ 3 times using for loop, while loop and do while loop
void main(){
  print("Printed by for loop:");
  for (int i=0;i<3;i++){
    print("Hello Sunway");
  }
  int i=0;
  print("Printed by while loop:");
  while(i<3){
    print("Hello Sunway");
    i++;
  }
  print("Printed by for do-whileloop:");
  do{
    print("Hello Sunway");
    i--;

  }
  while(i>0);
}
